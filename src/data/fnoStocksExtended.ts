function estimateLotSize(price: number): number {
  const target = 450_000;
  let lot = Math.round(target / price);
  lot = Math.max(25, Math.min(10000, lot));
  lot = Math.round(lot / 25) * 25;
  return lot || 25;
}

export function buildExtendedFnoStocks(existingSymbols: Set<string>): FnoInstrument[] {
  const seen = new Set(existingSymbols);
  const out: FnoInstrument[] = [];

  for (const [symbol, name, sector, basePrice] of RAW) {
    const sym = symbol.toUpperCase();
    if (seen.has(sym)) continue;
    seen.add(sym);
    out.push({
      symbol: sym,
      name,
      type: 'stock',
      sector,
      basePrice,
      lotSize: estimateLotSize(basePrice),
    });
  }
  return out;
}
